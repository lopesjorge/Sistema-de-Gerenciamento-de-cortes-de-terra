class Veiculo:
    def __init__(self,placa,modelo):
        self.placa = placa
        self.modelo = modelo
        
    def setPlaca (self, placa):
        self.placa = placa
        
    def setModelo (self, modelo):
        self.modelo = modelo
        
    def getPlaca (self):
        return self.placa
    
    def getModelo (self):
        return self.modelo
    
        
    